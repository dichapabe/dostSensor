package org.georepublic.bean;

/**
 * @author mbasa
 *
 */
public class WaterGauge {
    private String name;
    private String time;
    private double temp;
    private double humi;
    private double pres;
    private double rain;
    private double lon;
    private double lat;
    /**
     * @return the name
     */
    public String getName() {
        return name;
    }
    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }
    /**
     * @return the time
     */
    public String getTime() {
        return time;
    }
    /**
     * @param time the time to set
     */
    public void setTime(String time) {
        this.time = time;
    }
    /**
     * @return the temp
     */
    public double getTemp() {
        return temp;
    }
    /**
     * @param temp the temp to set
     */
    public void setTemp(double temp) {
        this.temp = temp;
    }
    /**
     * @return the humi
     */
    public double getHumi() {
        return humi;
    }
    /**
     * @param humi the humi to set
     */
    public void setHumi(double humi) {
        this.humi = humi;
    }
    /**
     * @return the pres
     */
    public double getPres() {
        return pres;
    }
    /**
     * @param pres the pres to set
     */
    public void setPres(double pres) {
        this.pres = pres;
    }
    /**
     * @return the rain
     */
    public double getRain() {
        return rain;
    }
    /**
     * @param rain the rain to set
     */
    public void setRain(double rain) {
        this.rain = rain;
    }
    /**
     * @return the lon
     */
    public double getLon() {
        return lon;
    }
    /**
     * @param lon the lon to set
     */
    public void setLon(double lon) {
        this.lon = lon;
    }
    /**
     * @return the lat
     */
    public double getLat() {
        return lat;
    }
    /**
     * @param lat the lat to set
     */
    public void setLat(double lat) {
        this.lat = lat;
    }
    
    
}
